    public void EditarUsuario(String us){
        String sql="Select * from usuarios where usuario like '"+us+"'";
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {                
                txtUsuario.setText(rs.getString(1));
                psfContraseña.setText(rs.getString(2));
                txtDni.setText(rs.getString(3));
                cmbEstado.setSelectedItem(rs.getString(4));
                cmbTipoUsuario.setSelectedIndex(rs.getInt(5)-1);
            }
            user=us;
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }

