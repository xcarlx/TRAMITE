    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String sql = null, msj;
        switch (Anuevo){
            case "NUEVO":
                sql="call nuevoUser(?,?,?,?,?);";
                msj="USUARIO REGISTRADO";
                break;
            case "MODIFICAR":
                sql="call actulizarUsuario(?,?,?,?,?,?)"; 
                msj="USUARIO MODIFICADO";
                break;
            default:
                msj="ERROR";
        } 
        if("NUEVO".equals(Anuevo)){
            try {
                PreparedStatement pst = cn.prepareCall(sql);
                pst.setString(1, txtUsuario.getText().toUpperCase());
                pst.setString(2, String.valueOf(psfContraseña.getPassword()));
                pst.setString(3, txtDni.getText());
                pst.setString(4, cmbEstado.getSelectedItem().toString());
                pst.setString(5, String.valueOf(cmbTipoUsuario.getSelectedIndex()+1));
                int n = pst.executeUpdate();
                if(n>0){
                    JOptionPane.showMessageDialog(rootPane, msj);
                    TablaUser("");
                    HI(false);
                }
            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(rootPane, e);
            }
        }
        if("MODIFICAR".equals(Anuevo)){
            try {
                PreparedStatement pst = cn.prepareCall(sql);
                pst.setString(1, txtUsuario.getText().toUpperCase());
                pst.setString(2, String.valueOf(psfContraseña.getPassword()));
                pst.setString(3, txtDni.getText());
                pst.setString(4, cmbEstado.getSelectedItem().toString());
                pst.setString(5, String.valueOf(cmbTipoUsuario.getSelectedIndex()+1));
                pst.setString(6, user);
                int n = pst.executeUpdate();
                if(n>0){
                    JOptionPane.showMessageDialog(rootPane, msj);
                    TablaUser("");
                    HI(false);
                }
            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(rootPane, e);
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

