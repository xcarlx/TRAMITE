    public void TablaUser(String var){
        String sql="select usuario, contraseña, dni, estado, tipo_u "
                + "from usuarios us inner join tipo_usuario tu "
                + "on us.idtipo_usuario = tu.idtipo_usuario where usuario like '%"+var+"%' OR dni like '%"+var+"%'";
        String titulos [] = {"USUARIO","CONTRASEÑA","DNI","ESTADO","TIPO"};
        String registro [] = new String[5];
        tabla_usuario = new DefaultTableModel(null,titulos);
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                registro[0]=rs.getString(1);
                registro[1]=rs.getString(2);
                registro[2]=rs.getString(3);
                registro[3]=rs.getString(4);
                registro[4]=rs.getString(5);
                tabla_usuario.addRow(registro);
            }
            tblUsuario.setModel(tabla_usuario);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
