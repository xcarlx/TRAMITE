    public void EditarTabla(String vdni, String vfecha_i) {
        String sql = "select * from historial_t where dni=" + vdni + " and fecha_inicio='" + vfecha_i + "'";

        try {
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
                txtDni.setText(rs.getString(1));
                dcFeInicio.setDate(rs.getDate(2));
                dcFeFin.setDate(rs.getDate(3));
                cmbEstado.setSelectedItem(rs.getString(4));
                cmbDependencia.setSelectedIndex(rs.getInt(5) - 1);
            }
            dni=vdni;
            fecha_i=vfecha_i;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }

