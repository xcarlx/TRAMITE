    public void TablaHisto(String vdni) {
        String sql = "select ht.dni,ht.fecha_inicio, ht.fecha_termino, ht.estado, dp.dependencia,pro.provincia "
                + "from historial_t ht inner join dependencias dp on ht.iddependencia=dp.iddependencia "
                + "inner join provincia pro on dp.idprovincia=pro.idprovincia and dp.iddepartamento = pro.iddepartamento"
                + " WHERE ht.dni="+vdni;
        String titulos[] = {"DNI", "F_INICIO", "F_FIN", "ESTADO", "DEPENDENCIA", "PROVINCIA"};
        String regitro[] = new String[6];
        tabla_histo = new DefaultTableModel(null, titulos);
        try {
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
                regitro[0] = rs.getString(1);
                regitro[1] = rs.getString(2);
                regitro[2] = rs.getString(3);
                regitro[3] = rs.getString(4);
                regitro[4] = rs.getString(5);
                regitro[5] = rs.getString(6);
                tabla_histo.addRow(regitro);
            }
            tblHistorial.setModel(tabla_histo);
            tblHistorial.getMaximumSize();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
