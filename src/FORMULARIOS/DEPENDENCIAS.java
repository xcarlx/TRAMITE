    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String sql = null, msj;
        switch (Anuevo){
            case "NUEVO":
                sql="call nuevaDepen(?,?,?,?,?)";
                msj="DEPENDENCIA AGREGADA";
                break;     
            case "MODIFICAR":
                sql="call actualizarDepen(?,?,?,?,?,?)";
                msj="DEPENDENCIA MODIFICADA";
                break;
            default:
                msj="ERROR";
                break;
        }
        if(Anuevo.equals("NUEVO")){
            try {
                PreparedStatement pst = cn.prepareCall(sql);
                pst.setString(1, txtDependencia.getText());
                pst.setString(2, txtIniciales.getText());
                pst.setString(3, txtDireccion.getText());
                pst.setInt(4, cmbProvincia.getSelectedIndex()+1);
                pst.setInt(5, cmbDepartamento.getSelectedIndex()+1);
                int n = pst.executeUpdate();
                if(n>0){
                    JOptionPane.showMessageDialog(rootPane, msj);
                    HI(false);
                    TablaDepen("");
                    OrderTabla();
                }
            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(rootPane, "ERROR AL INSERTAR LA DEPENDENCIA \n"+e);
            }
        }
        if(Anuevo.equals("MODIFICAR")){
            try {
                PreparedStatement pst = cn.prepareCall(sql);
                pst.setString(1, txtDependencia.getText());
                pst.setString(2, txtIniciales.getText());
                pst.setString(3, txtDireccion.getText());
                pst.setInt(4, cmbProvincia.getSelectedIndex()+1);
                pst.setInt(5, cmbDepartamento.getSelectedIndex()+1);
                pst.setString(6, iddepen);
                int n = pst.executeUpdate();
                if(n>0){
                    JOptionPane.showMessageDialog(rootPane, msj);
                    HI(false);
                    TablaDepen("");
                    OrderTabla();
                }
            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(rootPane, "ERROR AL MODIFICAR LA DEPENDENCIA \n"+e);
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void miEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miEditarActionPerformed
               int filasel;
       String id;
       String prov;
       String depa;
       try {
           filasel = tblDependencia.getSelectedRow();
           if(filasel == -1){
               JOptionPane.showMessageDialog(null, "no se a seleccionado niguna fila");
           }
           else{
               b=true;
               HI(b);
               Anuevo="MODIFICAR";
               tabla_depen = (DefaultTableModel) tblDependencia.getModel();
               id = (String) tabla_depen.getValueAt(filasel, 0);
               prov = (String) tabla_depen.getValueAt(filasel, 3);
               depa = (String) tabla_depen.getValueAt(filasel, 4);
               cmbDepartamento.setSelectedItem(depa);
               cmbProvincia.setSelectedItem(prov);
               EditarDepen(id);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        } 
    }//GEN-LAST:event_miEditarActionPerformed

    private void cmbDepartamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbDepartamentoActionPerformed
        cmbProvincia.removeAllItems();
        String sSQL="select provincia from provincia where "
                + "iddepartamento = "+String.valueOf(cmbDepartamento.getSelectedIndex()+1);
        try {
            Statement st1 = cn.createStatement();
            ResultSet rs1 = st1.executeQuery(sSQL);
            while(rs1.next()){
                cmbProvincia.addItem(rs1.getString(1));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_cmbDepartamentoActionPerformed

