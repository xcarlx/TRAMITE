    public void EditarDepen(String id){
        String sql="select dependencia, iniciales, direccion "
                + "from dependencias where iddependencia="+id;
        try {
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            while(rs.next()){
                txtDependencia.setText(rs.getString(1));
                txtIniciales.setText(rs.getString(2));
                txtDireccion.setText(rs.getString(3));
            }
            iddepen=id;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
