    public void TablaDepen(String dep){
        String sql="select depen.iddependencia, depen.dependencia, depen.direccion, pro.provincia, depa.departamento"
                + " from dependencias depen inner join provincia pro "
                + "on depen.idprovincia=pro.idprovincia and depen.iddepartamento=pro.iddepartamento"
                + " inner join departamento depa on pro.iddepartamento=depa.iddepartamento where depen.dependencia like '%"+dep+"%' ORDER BY pro.provincia";
        String titulos [] = {"ID","DEPENDENCIAS","DIRECCION","PROVINCIA","DEPARTAMENTO"};
        String registro [] = new String[5];
        tabla_depen = new DefaultTableModel(null, titulos);
        try {
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {                
                registro[0]= rs.getString(1);
                registro[1]= rs.getString(2);
                registro[2]= rs.getString(3);
                registro[3]= rs.getString(4);
                registro[4]= rs.getString(5);
                tabla_depen.addRow(registro);
            }
            tblDependencia.setModel(tabla_depen);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "ERROR AL CARGAR LA TALBA \n "+e);
        }
        
    }
