    public void CargarTablaDerivar(String asig){
        String titulo []={"ID","TIPO","NRO_DOC","FOLIOS","FECHA","REMITENTE",
            "OTROS","DEPENDENCIA"};
        String registro [] = new String[9];
        tabla_modelo = new DefaultTableModel(null,titulo);
        String sSQL="select doc.iddoc, tdoc.tipo, "
                + "doc.nro_doc,doc.folios, doc.fecha, doc.remitente, doc.otros,"
                + "dep.dependencia from documento doc "
                + "inner join derivar d on doc.iddoc= d.iddoc "
                + "inner join dependencias dep "
                + "on d.iddependencia_d = dep.iddependencia "
                + "inner join tipo_doc tdoc "
                + "on doc.idtipo_doc = tdoc.idtipo_doc "
                + "WHERE d.estado like 'INICIO'  AND dep.iddependencia like '"+this.getDependencia()+"'"
                + " AND doc.estado like 'PENDIENTE' and doc.usuario like '"+this.getUsuario()+"'"
                + " and d.asignado like '"+asig+"'";
        try {
            Statement st1 = cn.createStatement();
           ResultSet rs1 = st1.executeQuery(sSQL);
                while(rs1.next()){
                    registro[0]=rs1.getString(1);
                    registro[1]=rs1.getString(2);
                    registro[2]=rs1.getString(3);
                    registro[3]=rs1.getString(4);
                    registro[4]=rs1.getString(5);
                    registro[5]=rs1.getString(6);
                    registro[6]=rs1.getString(7);
                    registro[7]=rs1.getString(8);
                    tabla_modelo.addRow(registro);
                }
                tblDerivar.setModel(tabla_modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }  
    }

