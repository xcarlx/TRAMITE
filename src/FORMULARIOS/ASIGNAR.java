    private void btnDerivarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDerivarActionPerformed
        if(txaObservacion.getText().length()>0){
            int cont = tblDerivar.getRowCount();
            String id;
            if(cont > 0){
                for(int i=0;i<cont;i++){
                    id=(String) tblDerivar.getValueAt(i, 0);
                    String sSQL="UPDATE DERIVAR SET FECHA=?,ESTADO = ?, USUARIO = ?, OBSERVACIONES=? "
                            + "where iddependencia_d = "+this.getDependencia()+" AND estado like 'INICIO' and iddoc="+id;
                    try {
                        SimpleDateFormat sdf;   
                        sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                        PreparedStatement pst= cn.prepareStatement(sSQL);
                        pst.setString(1, sdf.format(f.Fecha()));
                        pst.setString(2, "DERIVADO");
                        pst.setString(3, this.getUsuario().toUpperCase());
                        pst.setString(4, txaObservacion.getText().toUpperCase());
                        pst.executeUpdate();
                    } catch (SQLException | HeadlessException e) {
                        JOptionPane.showMessageDialog(null , e);
                    }
                }
                CargarTablaDerivar(null);
                JOptionPane.showMessageDialog(null, "DOCUMENTOS DERIVADOS");
            }
            else{
                JOptionPane.showMessageDialog(null, "NO HAY NINGUN DOCUMENTO PARA DERIVAR");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "NO HA DIGITADO LA OBSERVACIÓN", "ERROR", 0);
        }
    }//GEN-LAST:event_btnDerivarActionPerformed

